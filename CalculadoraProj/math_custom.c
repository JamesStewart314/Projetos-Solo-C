#include "math_custom.h"

double soma(double val1, double val2) {
	return (val1 + val2);
}

double subtr(double val1, double val2) {
	return (val1 - val2);
}

double mult(double val1, double val2) {
	return (val1 * val2);
}

double divis(double val1, double val2) {
	return (val1 / val2);
}
